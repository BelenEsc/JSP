package belenaprende.com.CalculosMatematicos;

public class Calculos {
	static int resultado;

	public static int suma(int n1, int n2) {
		resultado = n1 + n2;
		return resultado;
	}

	public static int resta(int n1, int n2) {
		resultado = n1 - n2;
		return resultado;
	}

	public static int multi(int n1, int n2) {
		resultado = n1 * n2;
		return resultado;

	}
	
}
